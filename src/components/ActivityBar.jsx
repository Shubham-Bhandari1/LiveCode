const ACTIVITIES = [
  { id: 'files', Icon: FilesIcon, title: 'Explorer' },
  { id: 'search', Icon: SearchIcon, title: 'Search' },
  { id: 'git', Icon: GitIcon, title: 'Source Control' },
  { id: 'extensions', Icon: ExtensionsIcon, title: 'Extensions' },
  { id: 'users', Icon: UsersIcon, title: 'Collaborators' },
]

export default function ActivityBar() {
  const { activeActivity, setActiveActivity, toggleSidebar, sidebarOpen } = useEditorStore()

  const handleClick = (id) => {
    if (activeActivity === id && sidebarOpen) {
      toggleSidebar()
    } else {
      setActiveActivity(id)
      if (!sidebarOpen) toggleSidebar()
    }
  }

  return (
    <div className="activity-bar">
      {ACTIVITIES.map(({ id, Icon, title }) => (
        <button
          key={id}
          id={`activity-${id}`}
          className={`activity-btn ${activeActivity === id && sidebarOpen ? 'active' : ''}`}
          title={title}
          onClick={() => handleClick(id)}
        >
          <Icon />
        </button>
      ))}

      <div className="activity-bar-bottom">
        <button className="activity-btn" title="Settings" id="activity-settings">
          <SettingsIcon />
        </button>
      </div>
    </div>
  )
}
